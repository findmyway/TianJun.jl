function winings_after_step(T, init=INIT_WININGS)
	pre = copy(init)
	cur = fill!(similar(init), 0.)
	for _ in 1:T
		for (i, w) in zip(axes(pre,1), pre)
			if i == firstindex(pre) || i == lastindex(pre)
				cur[i] += pre[i]
			else
				for (j, v) in zip(axes(init,1), init)
					k = i + j
					cur[min(lastindex(init), max(firstindex(init), k))] += w * v
				end
			end
		end
		pre, cur = cur, pre
		fill!(cur, 0.)
	end
	pre
end

