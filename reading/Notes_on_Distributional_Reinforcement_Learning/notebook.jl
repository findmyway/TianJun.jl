# ╔═╡ 0a9aa3fb-6b70-4dba-b27d-e4c9545c971c
"""
	 ctd(env, θ, π, α, P, γ, e)

Algorithm 3.4: Online Categorical Temporal-Difference Learning

- `env`: the environment
- `θ`: supports
- `π`: the policy
- `α`: the learning rate
- `P`: a tabular reward distribution estimator
- `γ`: discount rate
- `e`: the number of episodes
"""
function ctd(env, θ, π, α, P, γ, e)
	for _ in 1:e
		reset!(env)
		while true
			x = state(env)
			a = π(x)
			env(a)
			r = reward(env)
			x′ = state(env)
			p = P[x]
			p′ = P[x′]
			p̂ = similar(p)
			fill!(p̂, 0)
			for j in 1:length(θ)
				g = r + γ * θ[j]
				if g ≤ θ[begin]
					p̂[begin] += p′[j]
				elseif g ≥ θ[end]
					p̂[end] += p′[j]
				else
					local I # alias of $i^{*}$
					for i in 1:length(θ)
						if θ[i] ≤ g ≤ θ[i+1]
							I = i
							break
						end
					end
					ζ = (g - θ[I])/(θ[I+1]-θ[I])
					p̂[I] += (1-ζ) * p′[j]
					p̂[I+1] += ζ * p′[j]
				end
			end
			p .= (1-α) .* p .+ α .* p̂
			is_terminated(env) && break
		end
	end
end

